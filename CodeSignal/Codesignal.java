String swapCase(String text) {
    String sol = "";
    int diff = 'a'-'A';
    for(char c:text.toCharArray()){
        if(c >='a' && c<='z')
            sol += (char) (c-diff)+"";
        else if(c >='A' && c<='Z')
            sol += (char) (c+diff)+"";
        else
            sol += c+"";
    }
    return sol;
}



