int crossingSum(int[][] matrix, int row, int column) {

    int result = 0;
    for (int i = 0; i < matrix.length; i++) {
      result += matrix[i][column]  ;
    }
    for (int i = 0; i < matrix[0].length; i++) {
      result += matrix[row][i];
    }
    result -= matrix[row][column];
  
    return result;
  }
  


