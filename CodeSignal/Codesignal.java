boolean isIdentityMatrix(int[][] matrix) {

    for (int i = 0; i < matrix.length; i++) {
      for (int j = 0; j < matrix.length; j++) {
        if (matrix[i][j] != 1 && i == j
            || matrix[i][j] != 0 && i != j) {
          return false;
        }
      }
    }
    return true;
  }
  

