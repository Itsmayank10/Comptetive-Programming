boolean isMonotonous(int[] sequence) {
    int flag = 0;
    if(sequence.length <2) return true;
    for(int i = 1; i<sequence.length; i++){
        if(sequence[i-1] <sequence[i]){
            flag++;
            
        }
        
        else if((sequence[i-1] >sequence[i]))
            flag--;
        else return false;
        
        
    }
    return Math.abs(flag) == sequence.length-1;
}



