int[] fractionReducing(int[] fraction) {
    int div = Math.min(fraction[0],fraction[1]);
    while(div !=1){
        if(fraction[0]%div == 0 && fraction[1]%div == 0){
            fraction[0]/=div;
            fraction[1]/=div;
            div = Math.min(fraction[0],fraction[1]);
        }
        else
            div--;
    }
    return fraction;
}



